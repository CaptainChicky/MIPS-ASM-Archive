# ------------------------
# FUNCTION: sin_cos
# ------------------------
# Calculates sine and cosine values using a lookup table
# Arguments:
#   a0 - angle in degrees
# Returns:
#   v0 - sin(a0) * 10000
#   v1 - cos(a0) * 10000
# Notes:
# - Uses cos(x) = sin(x + 90) identity
# - Values are scaled by 10000 for fixed-point arithmetic
sin_cos:
	add t0, a0, 90         # t0 = a0 + 90 (for cosine calculation)
	rem a0, a0, 360        # Normalize angle to [0, 360)
	rem t0, t0, 360        # Normalize cosine angle to [0, 360)
	mul a0, a0, 4          # Scale index by 4 (word size)
	mul t0, t0, 4          # Scale cosine index by 4 (word size)
	lw v0, sintable(a0)    # Load sin(a0) from table
	lw v1, sintable(t0)    # Load cos(a0) from table
	jr ra

